user = User.objects.filter(username="admin").first()


def load_tithes():
    for record in tithes:
        member = record.get("member")
        amount = record.get("amount")
        tithing_date = record.get("tithing_date")

        date_obj = format_date(tithing_date)
        month_name = get_month_name(date_obj.month)
        year = date_obj.year

        tithe = MemberTithing.objects.create(
            member_id=member,
            amount=amount,
            tithing_date=tithing_date,
            captured_by=user,
            month=month_name,
            year=year,
        )

        ChurchLedger.objects.create(
            name=f"{tithe.member.user.get_full_name()} Tithing",
            description=f"Tithing - {tithe.member.user.get_full_name()}",
            amount=Decimal(amount),
            direction="Income",
            user=user,
            transaction_date=tithing_date,
            month=month_name,
            year=year,
        )
        print(
            f"Created Tithe for {tithe.member.user.get_full_name()} on {tithing_date}"
        )
